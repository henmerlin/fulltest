package entidades.linha.tdm;

public class LinhaNortel{
	
	private String instancia;
	
	private String facilidade;
	
	private Integer ncos;
	
	private String custGroup;
	

	public LinhaNortel() {

	}

	public String getInstancia() {
		return instancia;
	}


	public void setInstancia(String instancia) {
		this.instancia = instancia;
	}


	public String getFacilidade() {
		return facilidade;
	}


	public void setFacilidade(String facilidade) {
		this.facilidade = facilidade;
	}


	public Integer getNcos() {
		return ncos;
	}


	public void setNcos(Integer ncos) {
		this.ncos = ncos;
	}

	public String getCustGroup() {
		return custGroup;
	}

	public void setCustGroup(String custGroup) {
		this.custGroup = custGroup;
	}

}
